# sort list
# int type
number = [4, 8, 3, 20, 2, 5, 1]
number.sort()  # ascending order
print(number)

number.sort(reverse = True)  # descending order
print(number)

# string type
eng = ["b", "u", "k", "a", "c", "x"]
eng.sort()  # ascending order
print(eng)

eng.sort(reverse = True)  # descendig order
print(eng)
