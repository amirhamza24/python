# In programming, type conversion is the process of converting data of one type to another. 
# For example: converting int data to str.

# string to int
num = "123542474"
print(int(num))
print(type(num))  # string type
print(type(int(num)))  # int type

# int to float
num2 = 50
print(num2)
print(type(num2))  # int type
print(float(num2))
print(type(float(num2)))  # float type
